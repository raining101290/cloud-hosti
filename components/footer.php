    /* -----------------------------
    ADD TO CART (AJAX)
    ------------------------------*/
    function addToCart(pid, billingcycle, promo, type = 'hosting') {

        // STEP 1: Load actual cart from backend
        fetch("includes/apis/cart/get-cart-raw.php")
            .then(res => res.json())
            .then(cartData => {

                const cart = cartData.cart || [];

                // STEP 2: Check hosting restriction
                if (type === 'hosting') {
                    const hasHosting = cart.some(item => item.type === 'hosting');

                    if (hasHosting) {
                        if (!confirm("You already selected a hosting plan. Replace it?")) {
                            return;
                        }

                        // STEP 3: Clear hosting items from BACKEND
                        fetch("includes/apis/cart/clear-hosting.php")
                            .then(() => {

                                // STEP 4: Add new hosting item
                                return fetch("includes/apis/cart/add-to-cart.php", {
                                    method: "POST",
                                    headers: {
                                        "Content-Type": "application/json"
                                    },
                                    body: JSON.stringify({
                                        pid,
                                        billingcycle,
                                        promo,
                                        type
                                    })
                                });
                            })
                            .then(r => r.json())
                            .then(() => loadCartDropdown());

                        return;
                    }
                }

                // STEP 5: Normal add-to-cart (no hosting conflict)
                fetch("includes/apis/cart/add-to-cart.php", {
                        method: "POST",
                        headers: {
                            "Content-Type": "application/json"
                        },
                        body: JSON.stringify({
                            pid,
                            billingcycle,
                            promo,
                            type
                        })
                    })
                    .then(r => r.json())
                    .then(() => loadCartDropdown());
            });
    }


    /* -----------------------------
    REMOVE ITEM FROM CART (AJAX)
    ------------------------------*/
    function removeCartItem(index) {

        fetch("includes/apis/cart/remove-cart-item.php", {
                method: "POST",
                headers: {
                    "Content-Type": "application/json"
                },
                body: JSON.stringify({
                    index
                })
            })
            .then(res => res.json())
            .then(data => {
                document.getElementById("cartCount").innerText = data.count;
                document.getElementById("cartCountMobile").innerText = data.count;
                loadCartDropdown();
            });
    }

    /* -----------------------------
    RENDER CART DROPDOWN (AJAX)
    ------------------------------*/
    function loadCartDropdown() {
        fetch("includes/apis/cart/get-cart.php")
            .then(res => res.json())
            .then(data => {
                document.getElementById("cartCount").innerText = data.count;
                document.getElementById("cartCountMobile").innerText = data.count;
                document.getElementById("cartDropdownContent").innerHTML = data.html;
            });
    }

    function processCheckout() {
        fetch("includes/apis/cart/get-cart-raw.php")
            .then(res => res.json())
            .then(data => {
                const encodedCart = btoa(JSON.stringify(data.cart));
                const selectedCurrency = document.body.getAttribute("data-current-currency");
                const whmcsCurrencyId = "<?php echo $whmcsCurrencyMap[$_SESSION['currency']] ?? 1; ?>";
                // console.log("Selected Currency: ", "https://portal.cloudhosti.com/cart-bridge.php?data=" + encodedCart + "&currency=" + whmcsCurrencyId);
                window.location.href =
                    "https://portal.cloudhosti.com/cart-bridge.php?data=" + encodedCart + "&currency=" + whmcsCurrencyId;
            });
    }

